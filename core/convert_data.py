def clean_num(val):
    if pd.isna(val) or val == "#N/A" or val == "":
        return 0
    return int(str(val).replace(',', '').strip())

df = pd.read_csv(io.StringIO(data), sep='\t')
df['26년 3월 매출'] = df['26년 3월 매출'].apply(clean_num)
df['25년 4월 매출'] = df['25년 4월 매출'].apply(clean_num)

# PREV_MONTH_SALES용 (26년 3월 매출)
prev_sales = {}
for _, row in df.iterrows():
    prev_sales[row['브랜드명']] = row['26년 3월 매출']

# MONTHLY_TM용 (25년 4월 매출, 없으면 26년 3월 매출)
monthly_tm = {}
for _, row in df.iterrows():
    val = row['25년 4월 매출'] if row['25년 4월 매출'] > 0 else row['26년 3월 매출']
    monthly_tm[row['브랜드명']] = val

