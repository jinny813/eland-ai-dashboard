for b, v in prev_sales.items():
    print(f'            "{b}": {v:,},'.replace(',', '_'))

print("\n--- MONTHLY_TM ---")
for b, v in monthly_tm.items():
    print(f'            "{b}": {v:,},'.replace(',', '_'))
