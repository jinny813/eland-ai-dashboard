def _normalize_month_key(month: str):
    """
    다양한 월 표현을 "YYYY_MM" 키로 정규화합니다.
    예)  "4월"       → "2026_04"  (현재 연도 기준)
         "26년 4월"  → "2026_04"
         "2026-04"  → "2026_04"
         "2026_04"  → "2026_04"
    """
    if not month:
        return None

    s = str(month).strip()

    # 이미 YYYY_MM 형식
    m = re.fullmatch(r'(\d{4})_(\d{1,2})', s)
    if m:
        return f"{m.group(1)}_{int(m.group(2)):02d}"

    # YYYY-MM 또는 YYYY/MM
    m = re.fullmatch(r'(\d{4})[-/](\d{1,2})', s)
    if m:
        return f"{m.group(1)}_{int(m.group(2)):02d}"

    # YY년 M월 또는 YYYY년 M월
    m = re.search(r'(\d{2,4})년\s*(\d{1,2})월', s)
    if m:
        yr = int(m.group(1))
        if yr < 100:
            yr += 2000
        return f"{yr}_{int(m.group(2)):02d}"

    # M월 (연도 없음 → 현재 연도)
    m = re.fullmatch(r'(\d{1,2})월', s)
    if m:
        yr = datetime.now().year
        return f"{yr}_{int(m.group(1)):02d}"

    return None

def normalize_brand_name(name: str) -> str:
    """브랜드명 괄호 제거 정규화 (예: '보브(VOV)' -> '보브', '로엠(ROEM)' -> '로엠')"""
    if not name:
        return ""
    return str(name).split('(')[0].strip()

def clean_store_name(name: str) -> str:
    """NC/뉴코아/동아/2001 수식어를 완벽하게 제거하는 표준 유틸리티"""
    if not name:
        return ""
    name = str(name).strip()
    name = name.replace("(진척중)", "").replace("[진척중]", "").replace("진척중", "").strip()
    for prefix in ["NC", "뉴코아", "동아", "2001"]:
        if name.startswith(prefix):
            name = name[len(prefix):].strip()
    return name

