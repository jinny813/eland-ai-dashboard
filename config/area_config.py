def _normalize(name: str) -> str:
    """브랜드명에서 괄호 및 내부 텍스트, 공백 제거 (매칭용)"""
    if not name: return ""
    # 괄호와 내부 텍스트 제거 (예: 베네통(영캐주얼) -> 베네통)
    s = re.sub(r'\(.*?\)', '', str(name))
    # 공백 및 영문 대소문자 통일
    return s.replace(' ', '').upper()

def get_area(store_name: str, brand_name: str) -> float:
    """지점 및 브랜드에 해당하는 평수를 반환합니다. (괄호 내용 무시 매칭 지원)"""
    store_cfg = AREA_CONFIG.get(store_name, {})
    if not store_cfg:
        return 0.0
        
    # 1. 완전 일치 확인
    if brand_name in store_cfg:
        return store_cfg[brand_name]
    
    # 2. 정규화 후 일치 확인 (괄호 무시)
    target_norm = _normalize(brand_name)
    for cfg_brand, area in store_cfg.items():
        if _normalize(cfg_brand) == target_norm:
            return area
            
    return 0.0
